using System.Activities;

namespace EmailHandlerWorkflow
{
    /// <summary>
    /// An activity that write messages in trace window.
    /// </summary>
    public sealed class TraceActivity : CodeActivity
    {
        // The text to be traced
        public InArgument<string> Text { get; set; }

        // If your activity returns a value, derive from CodeActivity<TResult>
        // and return the value from the Execute method.
        protected override void Execute(CodeActivityContext context)
        {
            // Obtain the runtime value of the Text input argument
            string text = context.GetValue(Text);

            System.Diagnostics.Trace.TraceInformation(text);
        }
    }
}
